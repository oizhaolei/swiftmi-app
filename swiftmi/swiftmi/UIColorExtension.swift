import UIKit

extension UIColor {
  
    /// VC主背景颜色
    class func themeBackgroundColor() -> UIColor{
        return UIColor(red: 255 / 255.0, green: 96 / 255.0, blue: 48 / 255.0, alpha: 1.0);
    }
    
}